čislo_dna = int(input("Zadaj číslo dňa:"))
if čislo_dna == 1:
    print("Pondelok")
elif čislo_dna == 2:
    print("Utorok")
elif čislo_dna == 3:
    print("Streda")
elif čislo_dna == 4:
    print("Štvrtok")
elif čislo_dna == 5:
    print("Piatok")
elif čislo_dna == 6:
    print("Sobota") 
elif čislo_dna == 7:
    print("Nedeľa")
else:
    print("Taký deň neexistuje!")