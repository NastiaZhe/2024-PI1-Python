a = int(input("zadaj čislo a:")) # input je príkazom vstupu. Vypíše na obrazovku text v zátvorke a to čo užívateľ napíše na klávesnice, uloží do premennej a
b = int(input("zadaj čislo b:")) # imput vracia vždy výsledok vo formáte reťazca (postupnosť znakov resp. text), funkcia int() pretypuje reťazec na celé číslo
sucet = a + b
print("Sucet je:", sucet)
sucin = a * b
print("Sucin je:", sucin)
rozdiel = a - b 
print("Rozdiel je:", rozdiel)
podiel = a / b
print("Podiel je:", podiel)